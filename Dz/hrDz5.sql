SELECT FIRST_NAME 
FROM EMPLOYEES
WHERE UPPER(FIRST_NAME) LIKE '%B%';

SELECT FIRST_NAME
FROM EMPLOYEES
WHERE UPPER(FIRST_NAME) LIKE '%A%A%';

SELECT FIRST_NAME, SUBSTR(FIRST_NAME,2,(LENGTH(FIRST_NAME)-2))
FROM EMPLOYEES;

SELECT JOB_ID, SUBSTR(JOB_ID,(INSTR(JOB_ID,'_')))
FROM EMPLOYEES
WHERE LENGTH(SUBSTR(JOB_ID,(INSTR(JOB_ID,'_'))))>3 AND SUBSTR(JOB_ID,(INSTR(JOB_ID,'CLERK')))!='CLERK';

SELECT *  FROM EMPLOYEES
