CASE
WHEN SALARY<10000 AND COMMISSION_PCT IS NULL THEN 'BAD'
WHEN SALARY BETWEEN 10000 AND 15000 OR commission_pct IS NOT NULL THEN 'NORMAL'
WHEN SALARY >=15000 THEN 'GOOD'
END AS INFO
FROM EMPLOYEES;

