SELECT TO_CHAR(SYSDATE+1,'"Tomorrow is" DAY "day of" MONTH') FROM DUAL;

SELECT FIRST_NAME, TO_CHAR(HIRE_DATE,'DDth "of" FMMONTH,YYYY') FROM EMPLOYEES;

SELECT SALARY, TO_CHAR((SALARY*0.2+SALARY),'$99,999.99') FROM EMPLOYEES;

SELECT FIRST_NAME,SALARY,SALARY+ TO_NUMBER('$12,345.55','$99,999.99') AS NEW_SALARY
FROM EMPLOYEES;
    
SELECT FIRST_NAME,HIRE_DATE,MONTHS_BETWEEN(TO_DATE('SEP, 18:45:00 18 2009','MON, HH24:MI:SS DD YYYY'),HIRE_DATE) AS NEW_DATE
FROM EMPLOYEES;

SELECT FIRST_NAME,COMMISSION_PCT,SALARY, TO_CHAR((SALARY+SALARY * NVL(commission_pct,0)),'$99,999.99') AS FULL_SALARY
FROM EMPLOYEES;

SELECT FIRST_NAME, LAST_NAME,NVL2(NULLIF(LENGTH(FIRST_NAME),LENGTH(LAST_NAME)),'different length','same length')
