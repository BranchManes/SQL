SELECT FIRST_NAME,COMMISSION_PCT,NVL2(COMMISSION_PCT,'YES','NO') AS INFO_COMMISSION_PCT
FROM EMPLOYEES;


SELECT FIRST_NAME,COALESCE(commission_pct,MANAGER_ID,SALARY)AS INFO
FROM EMPLOYEES;

SELECT FIRST_NAME,SALARY,
CASE
WHEN SALARY <5000 THEN 'Low level'
WHEN SALARY >=5000 AND SALARY <10000 THEN 'Normal level'
WHEN SALARY >=10000 THEN 'High levell'
END AS INFO
FROM EMPLOYEES;

SELECT COUNTRY_NAME,DECODE(REGION_ID,1,'Europe',2,'America',3,'Asia',4,'Africa') AS INFO
FROM COUNTRIES;

SELECT COUNTRY_NAME,
CASE REGION_ID
WHEN 1 THEN 'Europe'
WHEN 2 THEN 'America'
WHEN 3 THEN 'Asia'
WHEN 4 THEN 'Africa'
END AS INFO
FROM COUNTRIES;

